export type TranslationKey = keyof typeof en;

export const dictionaries = { en, ar } as const;

/** Substitute {placeholder} tokens in a translated string. */
export function format(template: string, vars?: Record<string, string | number>): string {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, k) => (k in vars ? String(vars[k]) : `{${k}}`));
}

export function translate(locale: Locale, key: TranslationKey, vars?: Record<string, string | number>): string {
  const dict = dictionaries[locale];
  const raw = dict[key];
  if (raw === undefined) {
    if (process.env.NODE_ENV !== 'production') {
      console.warn(`[i18n] Missing translation: ${locale}/${key}`);
    }
    return dictionaries.en[key] ?? key;
  }
  return format(raw, vars);
}

/** Convenience for plural-aware translation. Picks a plural key when count !== 1. */
export function translatePlural(
  locale: Locale,
  singularKey: TranslationKey,
  pluralKey: TranslationKey,
  count: number,
  vars: Record<string, string | number> = {},
): string {
  return translate(locale, count === 1 ? singularKey : pluralKey, { ...vars, count });
}

